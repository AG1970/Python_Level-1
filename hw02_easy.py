# Задача-2:
# Даны два произвольные списка.
# Удалите из первого списка элементы, присутствующие во втором списке.

#Решение
#Для формирования произвольных списков понадобился инструментарий:
import random

# Функцию переделал из найденного в сети генератора произвольного пароля
# https://ps.readthedocs.io/ru/latest/random.html 
def r_list(x):
    lst = []
    for i in range(x):
        lst.append(random.choice(list('123456789')))
    return lst


print("\nЗадача-2. Удаление элементов одного произвольного списка из другого\n")

lst1 = r_list(7)
lst2 = r_list(3)

print("Если из списка:\n", lst1)
print("удалить элементы списка:\n", lst2)
print("то получится список:\n", [n for n in lst1 if n not in lst2])

#Ещё одно альтернативное решение:
print ("\nЕсли необязательно сохранять порядок и повторяющиеся элементы,")
print ("то можно решить и с помощью множеств:")
print (list(set(lst1)-set(lst2))) 
