# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить 
# на два.    

print("\nЗадача-3. Преобразование списка*")

lst_old = r_list(5)
print("\nБыл список:\n", lst_old)
lst_new =[]

for i in lst_old:
    if float(i) % 2 == 0:
        lst_new.append(float(i) / 4)
    else:
        lst_new.append(float(i) * 2)

print("Новый список:\n", lst_new)
print("-"*3,"\n*) Четные элементы поделены на 4, а нечетные -- умножены на 2")
    
